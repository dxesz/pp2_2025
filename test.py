class MyClass():
    def __init__(self, name, age):
        self.name = name
        self.age = age
    def person1(self):
            print(f"Hi my name is {self.name}, and im {self.age}")

    def person2(self):
            print(f"Nice to meet you {self.name}!")

p1 = MyClass("John", 18)

p1.person1()
p1.person2()





        
        